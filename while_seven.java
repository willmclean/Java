
/**
 * Have the code write from zero to seven.
 *
 * @author (Will McLean)
 * @version (1.0)
 */
public class while_seven
{
    public static void main(String[] args)
    {
        System.out.println('\u000C');
        int myCount = 0;
        
        while (myCount < 8)
        {
            System.out.println(myCount);
            myCount++;
        }
    }
}
